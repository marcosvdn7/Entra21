package Lista4OOBRelacionamentoClasses;

public class ConversãoDeUnidadesDeTempo {

	public static double converterMinutoParaSegundo (double valor) {
		return valor * 60;
	}
	
	public static double converterHoraParaMinuto (double valor) {
		return valor * 60;
	}
	
	public static double converterDiaParaHora (double valor) {
		return valor * 24;
	}
	
	public static double converterSemanaParaDia (double valor) {
		return valor * 7;
	}
	
	public static double converterMesParaDia (double valor) {
		return valor * 30;
	}
	
	public static double converterAnoParaDia (double valor) {
		return valor * 365;
	}
}
